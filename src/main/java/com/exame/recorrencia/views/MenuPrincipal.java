    private void table1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table1MouseClicked
        if (table1.getSelectedRow() != -1) {
            AlbumDao dao = new AlbumDao("pu");
            CD cd = null;
            DVD dvd = null;
            btnRemoveAlbum.setEnabled(true);
            
            
            if (dao.findAll().get(table1.getSelectedRow()) instanceof CD){
                quantidaDeMusicas.setEnabled(true);
                resolucao.setEnabled(false);
                
                cd = (CD) dao.findAll().get(table1.getSelectedRow());
                nomeDoArtista.setText(cd.getNomeDoArtista());
                titulo.setText(cd.getTitulo());
                anoDeLancamento.setText(String.valueOf(cd.getAnoDeLancamento()));
                ritmo.setText(cd.getRitmo());
                preco.setText(String.valueOf(cd.getPreco()));
                quantidaDeMusicas.setText(cd.getQuantidadeDeMusicas()+"");
            }
            if (dao.findAll().get(table1.getSelectedRow()) instanceof DVD){
                dvd = (DVD)  dao.findAll().get(table1.getSelectedRow());
                quantidaDeMusicas.setEnabled(!true);
                resolucao.setEnabled(!false);;
                nomeDoArtista.setText(dvd.getNomeDoArtista());
                titulo.setText(dvd.getTitulo());
                anoDeLancamento.setText(String.valueOf(dvd.getAnoDeLancamento()));
                ritmo.setText(dvd.getRitmo());
                preco.setText(String.valueOf(dvd.getPreco()));
                resolucao.setSelectedItem(dvd.getResoucao());
            }
            
        }
    }//GEN-LAST:event_table1MouseClicked

