    private void btnSalvarAlbumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlbumActionPerformed
        AlbumDao dao = new AlbumDao("pu");

        if (table1.getSelectedRow() == -1) {
         if(tipoDeAlbum.getSelectedItem().equals("CD")) {

             Album cd = new CD(null, nomeDoArtista.getText(), titulo.getText(), 
                    Integer.parseInt(anoDeLancamento.getText()), ritmo.getText(), Double.parseDouble(preco.getText()),
                    Integer.parseInt(quantidaDeMusicas.getText()));

            try {
                dao.create(cd);
                JOptionPane.showMessageDialog(this, "Album CD salvo");
            } catch(Exception e) {
                System.err.println(e.getLocalizedMessage());
            }
         } else {
             Album cd = new DVD(null, nomeDoArtista.getText(), titulo.getText(), 
                    Integer.parseInt(anoDeLancamento.getText()), ritmo.getText(), Double.parseDouble(preco.getText()),
                    (String) resolucao.getSelectedItem());

            try {
                dao.create(cd);
                JOptionPane.showMessageDialog(this, "Album DVD salvo");
            } catch(Exception e) {
                System.err.println(e.getLocalizedMessage());
            }
         } 
   
        }else {
            
            if (dao.findAll().get(table1.getSelectedRow()) instanceof CD) {
                CD cd = (CD) dao.findAll().get(table1.getSelectedRow());
             cd.setId(cd.getId());
             cd.setNomeDoArtista(nomeDoArtista.getText());
             cd.setTitulo(titulo.getText());
             cd.setAnoDeLancamento(Integer.parseInt(anoDeLancamento.getText()));
             cd.setRitmo(ritmo.getText());
             cd.setPreco(Double.parseDouble(preco.getText()));
             cd.setQuantidadeDeMusicas(Integer.parseInt(quantidaDeMusicas.getText()));
             try {
                 dao.edit(cd);
                 JOptionPane.showMessageDialog(this, "Album CD editado");
             }catch(Exception e) {
                 JOptionPane.showMessageDialog(this, "O tipo de album nao pode ser alterado");
             }
            }
            
            if (dao.findAll().get(table1.getSelectedRow()) instanceof DVD) {
                DVD cd = (DVD) dao.findAll().get(table1.getSelectedRow());
             cd.setId(cd.getId());
             cd.setNomeDoArtista(nomeDoArtista.getText());
             cd.setTitulo(titulo.getText());
             cd.setAnoDeLancamento(Integer.parseInt(anoDeLancamento.getText()));
             cd.setRitmo(ritmo.getText());
             cd.setPreco(Double.parseDouble(preco.getText()));
             cd.setResoucao((String)resolucao.getSelectedItem());
             try {
                 dao.edit(cd);
                 JOptionPane.showMessageDialog(this, "Album DVD editado");
             }catch(Exception e) {
                 JOptionPane.showMessageDialog(this, "O tipo de album nao pode ser alterado");
             }
            }
            
        }
        addRowTable1();
    }//GEN-LAST:event_btnSalvarAlbumActionPerformed

