    private void addRowTable1() {
        AlbumDao dao = new AlbumDao("pu");
        
        DefaultTableModel modelo = (DefaultTableModel) table1.getModel();
        modelo.setNumRows(0);
        
        @SuppressWarnings("unchecked")
        List<Album> albuns = dao.findAll();
        //sim = receitas;        
       
        if(!albuns.isEmpty()){    
            totalAlbuns.setText(String.valueOf(dao.getCount()));
            
            String tipo = "";
            CD cd = null;
            DVD dvd = null;
            
            for(Album r: albuns) {
                    
                    if (r instanceof CD){
                        tipo = "CD";
                        cd = (CD) r;
                    }
                    if (r instanceof DVD) {
                        tipo = "DVD";
                        dvd = (DVD) r;
                    }
                    
                modelo.addRow(new String[]{
                    String.valueOf(r.getId()),
                    r.getNomeDoArtista(),
                    r.getTitulo(),
                    String.valueOf(r.getAnoDeLancamento()),
                    String.valueOf(r.getRitmo()),
                    String.valueOf(r.getPreco()),
                    tipo,
                    String.valueOf(cd == null ? 0: cd.getQuantidadeDeMusicas()),
                    String.valueOf(dvd == null ? "---": dvd.getResoucao())
                });
                        }
        }
    } 
    
    
